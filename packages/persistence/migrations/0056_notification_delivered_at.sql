-- Отметка вручения уведомления по offline-каналу (email-дайджест).
-- Заменяет оконную (lookbackMinutes) выборку в notification.dispatch:
-- окно, привязанное к времени claim'а джобы, теряло уведомления при задержке
-- воркера и дублировало письма при retry. Маркер даёт идемпотентность и
-- догон backlog'а после простоя.
--
-- ВАЖНО про блокировки: scripts/migrate.mjs выполняет весь файл в ОДНОЙ
-- транзакции, поэтому ACCESS EXCLUSIVE, взятый ALTER TABLE, держится до COMMIT.
-- user_notifications — одна из самых горячих таблиц (пишется на каждое
-- упоминание, читается на каждом открытии колокольчика), поэтому здесь
-- запрещены операции, переписывающие таблицу: любой UPDATE ... SET по всем
-- строкам на инсталляции с миллионами уведомлений держал бы эксклюзивную
-- блокировку минутами и удваивал физический размер таблицы.
--
-- Backfill исторического хвоста делается БЕЗ переписывания строк, через
-- «быстрый DEFAULT» (PostgreSQL 11+): ADD COLUMN с константным DEFAULT пишет
-- значение только в каталог (pg_attribute.attmissingval), существующие строки
-- физически не трогаются. Затем DEFAULT снимается, поэтому НОВЫЕ строки
-- получают NULL. Итог ровно тот, ради которого писался UPDATE-backfill:
--   * всё, что существовало до миграции, читается как delivered_at = -infinity
--     (уже обработано offline-каналом) — первый прогон после деплоя НЕ разошлёт
--     письма по всему историческому непрочитанному backlog'у;
--   * всё, что создано после миграции, имеет delivered_at IS NULL и попадает
--     в очередь доставки.
-- Обе команды — операции над каталогом, блокировка держится микросекунды.
ALTER TABLE user_notifications
  ADD COLUMN delivered_at timestamptz DEFAULT '-infinity';

ALTER TABLE user_notifications
  ALTER COLUMN delivered_at DROP DEFAULT;

-- Счётчик попыток offline-доставки. Транзиентный отказ (SMTP 5xx на конкретном
-- адресе, сбой резолвинга прав) не ставит маркер — строка возвращается в
-- очередь. Без ограничения это head-of-line blocking: выборка идёт по
-- created_at ASC с лимитом, поэтому «мёртвый» ящик одного пользователя,
-- накопив строк на весь батч, навсегда закрывал доставку ВСЕМУ тенанту.
-- По исчерпании лимита строка уходит в dead-letter (маркер ставится, деградация
-- считается в metadata прогона). Константный DEFAULT — тоже без переписывания.
ALTER TABLE user_notifications
  ADD COLUMN delivery_attempts integer NOT NULL DEFAULT 0;

-- Скан очереди доставки — один запрос на тенант (см. F4: был N+1 по пользователям).
-- Индекс ЧАСТИЧНЫЙ: в него попадают только строки, реально ждущие доставки.
-- После быстрого DEFAULT таких строк на момент миграции ноль, поэтому индекс
-- строится пустым и дальше остаётся маленьким (очередь, а не вся история).
CREATE INDEX IF NOT EXISTS user_notifications_tenant_pending_delivery_idx
  ON user_notifications (tenant_id, created_at, id)
  WHERE delivered_at IS NULL;
